 * Defines the custom item's heal on kill settings.
 */
@Serializable
data class UncomplicatedCustomItemHealOnKillSettings(
    /**
     * The amount to heal the item's holder by.
     */
    @SerialName("heal_amount")
    val healAmount: Float? = null,
    /**
     * Defines if the heal amount should be converted to ahp if health is full.
     */
    @SerialName("convert_to_ahp_if_full")
    val convertToAhpIfFull: Boolean? = null
)