package io.github.vxrpenter.ucs.uci.data

import io.github.vxrpenter.ucs.enums.secretlab.*
import io.github.vxrpenter.ucs.enums.CustomItem
import io.github.vxrpenter.ucs.enums.Role
import io.github.vxrpenter.ucs.uci.enums.EffectEvent
import io.github.vxrpenter.ucs.uci.enums.UCIItemType
import kotlinx.serialization.SerialName
import kotlinx.serialization.Serializable

/**
 * Defines the custom item's glow settings.
 */
@Serializable
data class UncomplicatedCustomItemGlowSettings(
    /**
     * The custom item's glow color (hex value).
     */
    @SerialName("glow_color")
    val glowColor: String
)

/**
 * Defines the custom item's life steal settings.
 */
@Serializable
data class UncomplicatedCustomItemLifeStealSettings(
    /**
     * The custom item's life steal amount.
     */
    @SerialName("life_steal_amount")
    val lifeStealAmount: Float,
    /**
     * The amount of health regenerated by the custom item.
     */
    @SerialName("life_steal_percentage")
    val lifeStealPercentage: Float
)

/**
 * Defines the custom item's effect settings.
 */
@Serializable
data class UncomplicatedCustomItemEffectSettings(
    /**
     * The effect event that should be triggered by the custom item.
     *
     * @see EffectEvent
     */
    @SerialName("effect_event")
    val effectEvent: EffectEvent? = null,
    /**
     * The effect applied by the custom item.
     *
     * @see EffectType
     */
    val effect: EffectType,
    /**
     * The intensity of the effect applied by the custom item.
     */
    @SerialName("effect_intensity")
    val effectIntensity: Byte,
    /**
     * The duration of the effect applied by the custom item.
     */
    @SerialName("effect_duration")
    val effectDuration: Float,
    /**
     * Defines if the duration of the effect applied by the custom item should be added to an already applied effect.
     */
    @SerialName("add_duration_if_active")
    val addDurationIfActive: Boolean? = null
)

/**
 * Defines the custom item's audio settings.
 */
@Serializable
data class UncomplicatedCustomItemAudioSettings(
    /**
     * The path of the ugg audio file.
     */
    @SerialName("audio_path")
    val audioPath: String? = null,
    /**
     * The distance the sound will be heard from.
     */
    @SerialName("audible_distance")
    val audibleDistance: Float? = null,
    /**
     * The volume the sound will be heard at
     */
    @SerialName("sound_volume")
    val soundVolume: Float? = null,
)

/**
 * Defines the custom item's explosive bullets settings.
 */
@Serializable
data class UncomplicatedCustomItemExplosiveBulletsSettings(
    /**
     * The radius the explosive bullet will apply damage at.
     */
    @SerialName("damage_radius")
    val damageRadius: Float? = null
)

/**
 * Defines the custom item's spawn item when detonated settings.
 */
@Serializable
data class UncomplicatedCustomItemSpawnItemWhenDetonatedSettings(
    /**
     * The type of item to be spawned on detonation
     */
    @SerialName("item_type")
    val itemType: CustomItem? = null,
    /**
     * The id of the item to spawn.
     *
     * @see CustomItem
     */
    @SerialName("item_id")
    val itemId: Int? = null,
    /**
     * Tje time till the item despawns.
     */
    @SerialName("time_till_despawn")
    val timeTillDespawn: Float? = null,
    /**
     * The chance to spawn the item.
     * This is applied using percentages with 0 standing for 0% and 100 for 100%.
     */
    val chance: Int? = null,
    /**
     * Defines if the item can be picked up.
     */
    val pickupable: Boolean? = null
)

/**
 * Defines the custom item's cluster settings.
 */
@Serializable
data class UncomplicatedCustomItemClusterSettings(
    /**
     * The item to spawn in a cluster.
     */
    @SerialName("item_to_spawn")
    val itemToSpawn: UCIItemType,
    /**
     * The number of items to spawn.
     */
    @SerialName("amount_to_spawn")
    val amountToSpawn: Int? = null,
    /**
     * The damage multiplier applied (if the item is e.g., a grenade)
     */
    @SerialName("scp_damage_multiplier")
    val scpDamageMultiplier: Float? = null,
    /**
     * The fuse time (if the item is e.g., a grenade)
     */
    @SerialName("fuse_time")
    val fuseTime: Float? = null
)

/**
 * Defines the custom item's switch role on use settings.
 */
@Serializable
data class UncomplicatedCustomItemSwitchRoleOnUseSettings(
    /**
     * The delay to switch roles after using the custom item.
     */
    val delay: Float? = null,
    /**
     * The type of role to switch over after using the custom item.
     *
     * @see Role
     */
    @SerialName("role_type")
    val roleType: Role? = null,
    /**
     * The id of the role to switch over after using the custom item.
     */
    @SerialName("role_id")
    val roleId: Int? = null,
    /**
     * The way the role will be spawned. This can only be used if the [roleType] is set to [Role.NORMAL].
     *
     * @see RoleSpawnFlags
     */
    @SerialName("spawn_flags")
    val spawnFlags: RoleSpawnFlags? = null,
    /**
     * Defines if the role should be spawned at the exact same position as the item's holder currently is.
     * This only works if [roleType] is [Role.ECR] or [Role.UCR]
     */
    @SerialName("keep_location")
    val keepLocation: Boolean? = null
)

/**
 * Defines the custom item's die on drop settings.
 */
@Serializable
data class UncomplicatedCustomItemDieOnDropSettings(
