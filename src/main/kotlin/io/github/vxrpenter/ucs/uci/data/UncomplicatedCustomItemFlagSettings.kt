/**
 * Defines the custom item's craftable settings.
 */
@Serializable
data class UncomplicatedCustomItemCraftableSettings(
    /**
     * The Scp914 knop setting to craft the custom item at.
     *
     * @see Scp914KnobSetting
     */
    @SerialName("knob_setting")
    val knobSetting: Scp914KnobSetting? = null,
    /**
     * The item the custom item will originate from.
     */
    @SerialName("original_item")
    val originalItem: UCIItemType? = null,
    /**
     * The chance that the [originalItem] will be turned into the custom item.
     * This is applied using percentages with 0 standing for 0% and 100 for 100%.
     */
    val chance: Int? = null
)

