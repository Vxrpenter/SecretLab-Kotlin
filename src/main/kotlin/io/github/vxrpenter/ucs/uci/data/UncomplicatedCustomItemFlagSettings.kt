 * Defines the custom item's can't drop settings.
 */
@Serializable
data class UncomplicatedCustomItemCantDropSettings(
    /**
     * Defines if a hint or broadcast should be displayed.
     */
    @SerialName("hint_or_broadcast")
    val hintOrBroadcast: String? = null,
    /**
     * The message to be displayed.
     */
    val message: String? = null,
    /**
     * The duration to display the message for.
     */
    val duration: Short? = null
)

/**
 * Defines the custom item's disguise settings.
 */
@Serializable
data class UncomplicatedCustomItemDisguiseSettings(
    /**
     * The type of role to disguise as.
     *
     * @see RoleType
     */
    @SerialName("role_id")
    val roleId: RoleType? = null,
    /**
     * The message sent when disguise is activated.
     */
